        public static double[] SolarDateData(DateTime d)
        {
            if (d < new DateTime(1901, 1, 1))
            {
                throw new ArgumentOutOfRangeException("Date cannot be less than 1/1/1901");
            }
            if (d > new DateTime(2100, 12, 31))
            {
                throw new ArgumentOutOfRangeException("Date cannot be greater than 12/31/2100");
            }
            if (d.Year >= 1901 && d.Year <= 2000)
            {
                return SE1901;
            }
            if (d.Year >= 2001 && d.Year <= 2100)
            {
                return SE2001;
            }
            throw new ArgumentOutOfRangeException("DateTime is invalid.");
        }
